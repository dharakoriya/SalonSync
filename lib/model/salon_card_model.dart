class SalonCard {
  final String image;
  final String salonName;
  final String address;
  final double rating;
  final int likeCount;

  SalonCard({
    required this.image,
    required this.salonName,
    required this.address,
    required this.rating,
    required this.likeCount, required Null Function() onTap,
  });
}
